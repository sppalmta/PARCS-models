      program read_dpl
      implicit none

      integer :: ia
      integer :: j, i1, jj
      real(8) :: xk, xp, a1, p1, p2, p3, a2, a3, a4, a5, a6
      real(8) :: slope, b

      integer :: nstate
      integer, parameter :: maxstate=100
      real(8) :: ppm(maxstate)
      real(8) :: days(maxstate)

      character(len=100) :: fname
      character(len=180) :: line

!--- read file name from command line

      ia=iargc()
      if (ia.eq.0) stop 'usage: readdpl [file.dpl]'

      call getarg(1,fname)
      open (12,file=fname,status='old',action='read')

      do
        read (12,'(a)') line
        if (line(1:20).eq.' ===================') exit
      enddo
      read (12,*)  ! titles
   
      nstate=0 
      do
        read (12,'(a)',end=500) line
        if (line(1:5).eq.' ____') exit
        read (line,*) j, i1, xk, xp, a1, p1, p2, p3, a2, days(j), a3, a4, a5, a6, ppm(j)
        nstate=nstate+1
        write (*,20) j, days(j), ppm(j)
        if (j.ne.nstate) write (0,*) 'ERROR: cycle number error'
      enddo
  500 continue
   20 format (i4,',',f6.1,',',f7.1)
      close (12)

!--- search for EOC

      jj=0
      do j=1, nstate
        if (ppm(j).lt.0.10001d0) then
          jj=j
          exit
        endif
      enddo
      if (jj.eq.0) jj=nstate+1
      j=jj-1
      slope=(ppm(j-1)-ppm(j))/(days(j-1)-days(j))
      b=ppm(j)-slope*days(j)
      write (*,30) -b/slope
   30 format (/,'estimated EOC ', f8.1,' days')
      end
 
